export const ENVEX_SEMINAR_DATE_FILTERS = [
    { id: 'all', label: '전체' },
    { id: '5.20(수)', label: '5.20(수)' },
    { id: '5.21(목)', label: '5.21(목)' },
    { id: '5.22(금)', label: '5.22(금)' },
] as const;

export type EnvexSeminarDateFilterId = (typeof ENVEX_SEMINAR_DATE_FILTERS)[number]['id'];

export function filterEnvexSeminars(
    items: EnvexSeminarItem[],
    options: { dateFilter?: EnvexSeminarDateFilterId; query?: string },
): EnvexSeminarItem[] {
    const { dateFilter = 'all', query = '' } = options;
    const q = query.trim().toLowerCase().replace(/\s+/g, '');

    return items.filter((item) => {
        if (dateFilter !== 'all' && item.date !== dateFilter) return false;
        if (!q) return true;

        const haystack = [item.title, item.organizer, item.date, item.time, item.place]
            .join(' ')
            .toLowerCase()
            .replace(/\s+/g, '');

        return haystack.includes(q);
    });
}
